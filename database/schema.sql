CREATE DATABASE IF NOT EXISTS NearbyHomes;

USE NearbyHomes;

-- Users table
CREATE TABLE IF NOT EXISTS users (
    id INT AUTO_INCREMENT PRIMARY KEY,
    first_name VARCHAR(100),
    last_name VARCHAR(100),
    email VARCHAR(100) UNIQUE,
    password VARCHAR(100),
    role ENUM('client', 'agent', 'admin')
);

-- Apartments table
CREATE TABLE IF NOT EXISTS apartments (
    id INT AUTO_INCREMENT PRIMARY KEY,
    agent_id INT,
    title VARCHAR(200),
    address TEXT,
    price DECIMAL(10,2),
    bedrooms INT,
    latitude DECIMAL(10,7),
    longitude DECIMAL(10,7),
    listed_date DATETIME DEFAULT NOW(),
    FOREIGN KEY (agent_id) REFERENCES users(id)
);

CREATE INDEX idx_address ON apartments (address);
CREATE INDEX idx_price ON apartments (price);
CREATE INDEX idx_bedrooms ON apartments (bedrooms);

-- Add a table for storing contact messages
CREATE TABLE IF NOT EXISTS contact_messages (
    id INT AUTO_INCREMENT PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    email VARCHAR(100) NOT NULL,
    message TEXT NOT NULL,
    submitted_at DATETIME DEFAULT NOW()
);

